  WHEN delivery_status = 'مستلم مسبقا' THEN '35'
  WHEN delivery_status = 'الرقم غير معرف' THEN '36'
  WHEN delivery_status = 'الرقم غير داخل في الخدمة' THEN '37'
  WHEN delivery_status = 'العنوان غير دقيق' THEN '38'
  WHEN delivery_status = 'لم يطلب' THEN '39'
  WHEN delivery_status = 'حظر المندوب' THEN '40'
  WHEN delivery_status = 'لا يمكن الاتصال بالرقم' THEN '41'
  WHEN delivery_status = 'تغيير المندوب' THEN '42'
  WHEN delivery_status = 'اخراج من المخزن وارسالة الى الفرز' THEN '44'
  ELSE delivery_status
END
WHERE delivery_partner = 'alwaseet'
  AND delivery_status !~ '^\d+$';