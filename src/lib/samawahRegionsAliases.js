/**
 * الحصول على المرادفات لمنطقة معينة
 */
export const getRegionAliases = (regionName) => {
  return samawahRegionAliases[regionName] || null;
};

/**
 * الحصول على جميع المناطق مع مرادفاتها
 */
export const getAllRegionsWithAliases = () => {
  return Object.values(samawahRegionAliases);
};

/**
 * البحث عن منطقة من خلال المرادف
 */
export const findRegionByAlias = (alias) => {
  const normalizedAlias = alias.toLowerCase().trim();
  
  for (const [regionName, data] of Object.entries(samawahRegionAliases)) {
    if (regionName.toLowerCase() === normalizedAlias) {
      return { region: regionName, confidence: 1.0, type: 'exact' };
    }
    
    const matchedAlias = data.aliases.find(
      a => a.text.toLowerCase() === normalizedAlias
    );
    
    if (matchedAlias) {
      return {
        region: regionName,
        confidence: matchedAlias.confidence,
        type: matchedAlias.type,
        alias: matchedAlias.text
      };
    }
  }
  
  return null;
};
